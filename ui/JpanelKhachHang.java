    private void search() {
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);
        String keyword = txtTim.getText();
        List<KhachHang> list = dao.select();

        for (KhachHang kh : list) {
            // contains: kiểm tra xem từ khóa có trùng khớp với list hay ko
            if (kh.getMaKH().contains(keyword)
                    || kh.getTenKH().contains(keyword)
                    || kh.getSoDT().contains(keyword)
                    || kh.getCCCD().contains(keyword)) {
                Object[] row = {
                    kh.getMaKH(),
                    kh.getTenKH(),
                    kh.getSoDT(),
                    kh.getCCCD(),
                    kh.isGioiTinh() ? "Nữ" : "Nam",
                    kh.getDiaChi()};
                model.addRow(row);
            }

        }
    }


