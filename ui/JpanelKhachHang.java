    public void initData() {
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);
        try {
            List<KhachHang> list = dao.select();
            for (KhachHang kh : list) {
                Object[] row = {
                    kh.getMaKH(),
                    kh.getTenKH(),
                    kh.getSoDT(),
                    kh.getCCCD(),
                    kh.isGioiTinh() ? "Nữ" : "Nam",
                    kh.getDiaChi()};

                model.addRow(row);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    public void find() {
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);
        try {
            String keyword = txtTim.getText();
            List<KhachHang> list = dao.selectByKeyword(keyword);
            for (KhachHang kh : list) {
                Object[] row = {
                    kh.getMaKH(),
                    kh.getTenKH(),
                    kh.getSoDT(),
                    kh.getCCCD(),
                    kh.isGioiTinh() ? "Nữ" : "Nam",
                    kh.getDiaChi()};

                model.addRow(row);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

